//
// A service wants to switch to snapshot.  Only do so if Raft hasn't
// have more recent info since it communicate the snapshot on applyCh.
//
func (rf *Raft) CondInstallSnapshot(lastIncludedTerm int, lastIncludedIndex int, snapshot []byte) bool {
	// Your code here (2D).
	rf.lock("CondInstallSnapshot()")
	rf.unlock("exiting CondInstallSnapshot()")
	if !rf.snapshotUTD {
		return false
	}

	// relIndex := rf.relativeIndex(lastIncludedIndex)

	// cut log entries according to the rules.
	if rf.containsIndexTerm(lastIncludedIndex, lastIncludedTerm) {
		rf.log = rf.log[rf.relativeIndex(lastIncludedIndex+1):]
	} else {
		rf.log = make([]LogEntry, 0)
	}
	rf.persist()
	// install coming snapshot.(install是必然要install的)
	rf.lastInstalledIndex = lastIncludedIndex
	rf.lastInstalledTerm = lastIncludedTerm
	rf.snapshot = snapshot
	rf.persist()
	// 照理来说，snapshot包括的部分应该被视为committed and applied.
	rf.commitIndex = max(rf.commitIndex, lastIncludedIndex) // 先这么写着
	rf.lastApplied = max(rf.lastApplied, lastIncludedIndex)
	return true
}

// the service says it has created a snapshot that has
// all info up to and including index. this means the
// service no longer needs the log through (and including)
// that index. Raft should now trim its log as much as possible.
func (rf *Raft) Snapshot(index int, snapshot []byte) {
	// Your code here (2D).
	rf.lock("snapshot received from service")
	defer rf.unlock("exiting snapshot()")
	rf.snapshot = snapshot
	rf.persist()
	// rf.snapshotUTD = true
	relIndex := rf.relativeIndex(index)
	rf.lastInstalledIndex = index
	rf.lastInstalledTerm = rf.log[relIndex].Term
	if relIndex == len(rf.log)-1 {
		rf.log = make([]LogEntry, 0)
	} else {
		rf.log = rf.log[relIndex+1:]
	}
	rf.persist()
}

//
// the service using Raft (e.g. a k/v server) wants to start
// agreement on the next command to be appended to Raft's log. if this
// server isn't the leader, returns false. otherwise start the
// agreement and return immediately. there is no guarantee that this
// command will ever be committed to the Raft log, since the leader
// may fail or lose an election. even if the Raft instance has been killed,
// this function should return gracefully.
//
// the first return value is the index that the command will appear at
// if it's ever committed. the second return value is the current
// term. the third return value is true if this server believes it is
// the leader.
//
func (rf *Raft) Start(command interface{}) (int, int, bool) {
	// Your code here (2B).
	if rf.killed() {
		return -1, -1, false
	}
	rf.lock("start lock")
	defer rf.unlock("start lock")
	if rf.identity != "leader" {
		return -1, rf.currentTerm, false
	}

	index := rf.absoluteLength()
	term := rf.currentTerm
	isLeader := true
	// to pass the last test, the new entry must be appended instantly.
	rf.log = append(rf.log, LogEntry{Command: command, Term: rf.currentTerm})
	rf.persist()
	//logLength := len(rf.log)

	//go rf.startAgreement(command, logLength)
	return index, term, isLeader
}

